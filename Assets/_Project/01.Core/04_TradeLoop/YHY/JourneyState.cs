// =============================================================================
// [작성] 윤호영
//
// [핵심 포인트]
//  · 이 enum은 "지금 무역이 어느 단계냐"만 담는다. (성공/실패 판정 아님)
//  · 성공/부분성공/실패는 JourneyResultData의 등급(JourneyResultGrade)으로 처리한다.
//    → 실패해도 단계는 Settling을 거쳐 Completed로 간다. "무슨 결과였나"는 결과 데이터가 말한다.
//  · 흐름: Prepare → Traveling → Settling → Completed → (다시 Prepare)
//  · 출발 검증은 "출발 시도" 한 동작에서 처리하므로 Ready 단계를 따로 두지 않는다.
//  · 저장에 직렬화되는 값이라 변경 시 저장 버전에 영향.
// =============================================================================

/// <summary>무역(상단)의 진행 단계.</summary>
public enum JourneyState
{
    Prepare,     // 준비: 상단 구성 중
    Traveling,   // 이동 중: 시간 경과 + 이벤트 발생 구간
    Settling,    // 정산 대기: 도착(또는 실패 확정) 후 결과 계산·수령 대기
    Completed    // 완료: 정산 수령까지 끝남 (이후 다시 Prepare)
}